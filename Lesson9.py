x = 10
y = 2
z = 10

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)

#関係演算子
print(x > y)
print(x < y)

print(x <= y)
print(x >= z)

#等価
print(x == y)
print(x != y)

#論理演算子
a = 8
b = 3
print(a >= 5 and a <= 10)
print(b >= 5 and b <= 10)
print(a == 3 or b == 3)
print(a == 1 or b == 1)

#代入演算子
c = 8
d = 12
e = 20

c += 10
e += d

print(c)
print(e)
